package Java.Basicos;

public class pruebas {
    public static void main(String[] args) {
        int a = 4;
        switch (a) {
            case 4:
                System.out.println("Es 4");
                break;
            case 5:   
                System.out.println("Es 5");
                break;
            default:
                System.out.println("No es ni 4 ni 5");
                break;
        }
    }
}
